import java.util.*;

public class BST {
    class tNode {
        int key;
        tNode left;
        tNode right;

        //Constructor with arguement

        public tNode(int key) {
            this.key = key;
            this.left = null;
            this.right = null;
        }

        //Constructor without arguement
        public tNode() {
            this.left = null;
            this.right = null;
        }

    }

    //BST CLass
}